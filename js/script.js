// Функція для генерації списку покупок
function generateShoppingList() {
    const shoppingListContainer = document.getElementById("shoppingListContainer");
    const shoppingList = document.getElementById("shoppingList");

    // Показуємо контейнер
    shoppingListContainer.style.display = "block";

    // Очищаємо список, якщо він вже існує
    shoppingList.innerHTML = "";

    // Додаємо кожен продукт до списку
    allProducts.forEach(product => {
        const listItem = document.createElement("li");

        // Створюємо чекбокс
        const checkbox = document.createElement("input");
        checkbox.type = "checkbox";
        checkbox.id = product;

        // Створюємо мітку
        const label = document.createElement("label");
        label.htmlFor = product;
        label.textContent = product;

        // Додаємо чекбокс і мітку до пункту списку
        listItem.appendChild(checkbox);
        listItem.appendChild(label);

        // Додаємо пункт до списку
        shoppingList.appendChild(listItem);
    });
}

// Додаємо слухача до кнопки "Згенерувати корзину"
document.getElementById("generateCartButton").addEventListener("click", generateShoppingList);

// Масив для асоціативних правил (приклад)
const associativeRules = [
    { if: "Молоко", then: "Чай, Борошно, Яйця"},
    { if: "Хліб", then: "Сир, Молоко, Ковбаса"},
    { if: "Картопля", then: "Куряче філе, Морква, Цибуля, Яйця"},
    { if: "Печиво", then: "Чай, Молоко, Кава"},
    { if: "Макарони", then: "Сир" },
    { if: "Крупи", then: "Цибуля, Морква, Куряче філе"},
    { if: "Куряче філе", then: "Борошно, Крупи, Картопля, Цибуля"},
    { if: "Яйця", then: "Борошно, Молоко" },
];

// Функція для пошуку асоціацій серед вибраних продуктів
function findAssociations(selectedProducts) {
    const associatedProducts = [];

    associativeRules.forEach(rule => {
        if (selectedProducts.includes(rule.if)) {
            associatedProducts.push({
                if: rule.if,
                then: rule.then,
            });
        }
    });

    return associatedProducts;
}

// Функція для відображення вибраних продуктів та асоціацій
function displaySelectedProducts() {
    const selectedProductsContainer = document.getElementById("selectedProductsContainer");
    const selectedProductsList = document.getElementById("selectedProductsList");

    // Очищаємо контейнер
    selectedProductsList.innerHTML = "";

    // Отримуємо вибрані продукти
    const selectedProducts = Array.from(document.querySelectorAll("#shoppingList input:checked"))
        .map(checkbox => checkbox.id);

    // Якщо вибрано продукти, відображаємо контейнер
    if (selectedProducts.length > 0) {
        selectedProductsContainer.style.display = "block";

        // Знаходимо асоціації
        const associations = findAssociations(selectedProducts);

        // Виводимо вибрані продукти
        const selectedHeader = document.createElement("li");
        selectedHeader.innerHTML = `<strong>Вибрані продукти:</strong> ${selectedProducts.join(", ")}`;
        selectedProductsList.appendChild(selectedHeader);

        // Виводимо асоціації
        associations.forEach(association => {
            const associationItem = document.createElement("li");
            associationItem.innerHTML = `
                <strong>Асоціативні продукти:</strong>,
                <strong></strong> ${association.if}, 
                <strong></strong> ${association.then}, 
            `;
            selectedProductsList.appendChild(associationItem);
        });
    } else {
        selectedProductsContainer.style.display = "none";
    }
}

// Додаємо слухача для чекбоксів
document.getElementById("shoppingList").addEventListener("change", displaySelectedProducts);